
// load the iostream library
#include <iostream>

// main function that returns an integer
int main() {

    // print "Hello, World!" to the console
    std::cout << "Hello, World!";

    // note that there is no end of the line
    std::cout << "Hello, World!";
    std::cout << std::endl;

    std::cout << "Line 1";
    std::cout << std::endl;

    std::cout << "Line 2\n";
    
    std::cout << "Line 3" << std::endl;

    // return 0 to indicate that the program has run successfully
    return 0;
}
